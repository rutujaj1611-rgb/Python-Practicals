a = 10
b = 3

# Arithmetic Operators

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)


# Comparison Operators

print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

# # Logical Operators

print("True and False:", True and False)
print("True or False:", True or False)
print("not True:", not True)

# # Assignment Operators

c = a
c += b
print("c += b:", c)
c -= b
print("c -= b:", c)
c *= b
print("c *= b:", c)

# # Bitwise Operators

print("a & b:", a & b)
print("a | b:", a | b)
print("a ^ b:", a ^ b)
print("~a:", ~a)
print("a << 1:", a << 1)
print("a >> 1:", a >> 1)

# # Membership Operators

list1 = [1, 2, 3, 4]
print("2 in list:", 2 in list1)
print("5 not in list:", 5 not in list1)

# # Identity Operators

x = [1, 2]
y = x
z = [1, 2]
print("x is y:", x is y)
print("x is z:", x is z)